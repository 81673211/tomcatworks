    public void addHeader(String name, String value) {
        final String lowerCaseName = name.toLowerCase();
        synchronized (headers) {
            List<String> values = headers.get(lowerCaseName);
            if (values == null) {
                values = new ArrayList<>();
                headers.put(name, values);
            }
            values.add(value);
        }
    }

